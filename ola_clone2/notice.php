    <div>
        <h5><b> Q: What is OLA’s peak time charge?</b></h5>
<p>OLA peak time charge is a fare-rationalizing rule of using a multiplier X or a flat
    surcharge to the standard fares causing the fare to be higher than the standard
    fare during certain times the day; i.e. if X is 1.2 during some point of time then
    the standard fare will be multiplied by 1.2 to get the final fare.
</p>
    </div>

    <div>
        <h5><b> Q: Why is there a need to apply a surcharge to my standard fare?</b></h5>
<p>OLA being a marketplace doesn’t control the number of vehicles online on its system/platform;
    drivers & operators are independent & work as per their convenience.
    During a normal day when demand & supply are met, there is no need to put an extra
    effort to pull the supply side of vehicles.</p>
        <p>
            OLA has observed that during certain times of the day, when more traffic is on road, routine drivers tend to log out of the system to suit their convenience as there is no incentive for them to put an extra effort. Similarly, when demand suddenly increases, not enough drivers might be available on the road. Customers might be inconvenienced by not being able to find cabs during such times.

        </p>

        <p>
            As a system/solutions provider OLA has limited options to motivate the drivers and cater to the high demand timings & scenarios. OLA has observed that the drivers are very sensitive to little extra earnings for their extra effort to stay logged in to our system.
        </p>

        <p>OLA applies an X multiplier or flat surcharge to the normal fare based on various situations and scenarios like place of pickup, time of pickup, type of vehicle, demand
            situation, supply scenario etc. Using advanced analytics, unique algorithm, past ride experiences etc. the OLA software calculates an appropriate surcharge to apply
            over and above the normal fare. This enables OLA to come up with appropriate incentives to drivers to make them stay logged in to the system for the additional
            earnings. This enables customers to get their much needed service even during peak times when demand for cabs is high.
The peak time surcharge works in two ways
</p>

        <p>The peak time surcharge works in two ways</p>

        <h5><b>a) It motivates drivers to stay & serve for a longer time even during difficult hours of the day and when demand is high.</b></h5>
        <h5><b>b) It filters out the less needed rides hence making the vehicle available to those who really need a cab.</b></h5>
    </div>

    <div>
        <h5><b>Transparency</b></h5>
<p>OLA is transparent about the rates. OLA makes sure that customers are aware of the dynamics of peak time charge and how it works. Whenever peak time charge is applicable;
        customers get a clear intimation, to confirm their intent to avail the ride at a higher price than normal. The booking process is initiated only when they confirm;
        hence, if a customer wants to avoid that ride he/she may choose to do it, thus making the cab available to others who are in need.
</p>
