        <h3> Peak Time Charge</h3>
<h5><b>Before knowing about OLA’s peak time charge, it is important for you to know how OLA works.
    OLA’s business model</b></h5>
        <p>OLA is an online marketplace for drivers.</p>
<p>These drivers are independent and they are either self-employed (own their commercial vehicles), or work for an Operator who owns multiple cars.</p>

        <p>Like you have an OLA app on your smart phone to book a cab, these drivers have a similar Driver mobile App on their smart phones. We activate the Driver app after a
            thorough check of authenticity and conducting due diligence of the commercial & personal papers of the driver & operator. These drivers decide their own time to login
            to the OLA application and accept requests for rides from customers. They may choose to remain logged out of the system as per their convenience.
        </p>
